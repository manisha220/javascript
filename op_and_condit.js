//Operators
//ARITHMETIC
let a=5;
let b=5;
console.log(a+b);
console.log(a-b);
console.log(a*b);
console.log(a/b);
console.log(a%b);
console.log(a**b); //exponentiation
console.log(a++); //post increment
console.log(++a); //pre increment
console.log(b--); //post decrement
console.log(--b); //pre decrement

